import { useState, useEffect } from 'react';
import axios from 'axios';
import styles from './MyProfile.module.css';
import { toast } from 'react-toastify';
import 'bootstrap/dist/css/bootstrap.min.css';

const MyProfile = () => {
  const [profile, setProfile] = useState({
    username: '',
    email: '',
    proffession: '',
    country: '',
    state: '',
    phone: '',
    profilePic: '',
    coverPhoto: '',
  });

  const [isLoading, setIsLoading] = useState(false);

  useEffect(() => {
    const fetchProfile = async () => {
      try {
        setIsLoading(true);
        const $FSA_auth_token = localStorage.getItem('$FSA_auth_token');
        const response = await axios.get('http://localhost:3000/myProfile', {
          headers: {
            Authorization: `Bearer ${$FSA_auth_token}`,
          },
        });

        setProfile({
          username: response.data.username,
          email: response.data.email,
          proffession: response.data.proffession,
          country: response.data.country,
          phone: response.data.phone,
          profilePic: response.data.profilePic, // URL of profile picture
          coverPhoto: response.data.coverPhoto, // URL of cover photo
        });
        setIsLoading(false);
      } catch (error) {
        console.error('Error fetching profile data:', error);
        toast.error('Failed to fetch profile data.');
      }
    };

    fetchProfile();
  }, []);

  return (
    <div className={styles.profileContainer}>
      {isLoading && <div className={styles.loader}>Loading...</div>}
      {!isLoading && (
        <>
          <div className={styles.coverPhotoWrapper}>
            <img src={profile.coverPhoto || 'https://static.vecteezy.com/system/resources/thumbnails/038/854/977/small_2x/ai-generated-dark-black-geometric-grid-background-modern-dark-abstract-texture-seamless-pattern-photo.jpg'} alt="Cover" className={styles.coverPhoto}/>
          </div>
          <div className={styles.profileWrapper}>
            <img src={profile.profilePic || '/images/lion.png'} alt="Profile" className={styles.profilePic}/>
          </div>
          <br />
          <div className={styles.parent}>
            <div className={styles.div1}>
              <h2 className={styles.username}>{profile.username.toUpperCase()}</h2>
              <h3 className={styles.info}>{profile.proffession}Student</h3>
              <p className={`${styles.info} ${styles.countryColour}`}>{profile.state}Kerala, India{profile.country}</p>
            </div>
            </div>

            <hr className={styles.separator} />
            <div className={styles.parent2}>
            <div className={styles.div2}>
              <p className={styles.info}>Email ID (Private to you)<i className="bi bi-envelope-at-fill"></i></p>
              <p className={`${styles.info} ${styles.txtDarker}`}>{profile.email}</p>
            </div>
            <div className={styles.div3}>
              <p className={`${styles.info}`}>Mobile no</p>
              <p className={`${styles.info} ${styles.txtDarker}`}> {profile.phone}9139293292</p>
            </div>
          </div>
        </>
      )}
    </div>
  );
  
};

export default MyProfile;

